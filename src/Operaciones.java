public class Operaciones {

	/**
	 * 
	 * @param numero1
	 * @param numero2
	 * @return Recibes dos parametros de tipo int los cuales se suman y devuelve
	 *         el resultado del tipo int
	 */
	public int sumar(int numero1, int numero2) {
		int res;
		res = numero1 + numero2;
		return res;
	}

}
